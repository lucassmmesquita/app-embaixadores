#!/usr/bin/env node
/**
 * Generate firebase-messaging-sw.js from environment variables.
 * Run this BEFORE `expo export` or `expo start`.
 *
 * Usage: node scripts/generate-firebase-sw.js
 *
 * Reads from EXPO_PUBLIC_FIREBASE_* env vars (or .env file via dotenv).
 */

const fs = require('fs');
const path = require('path');

// Try to load .env if dotenv is available
try {
  require('dotenv').config({ path: path.resolve(__dirname, '..', '.env') });
} catch {
  // dotenv not installed, rely on process.env
}

const config = {
  apiKey: process.env.EXPO_PUBLIC_FIREBASE_API_KEY || '',
  authDomain: process.env.EXPO_PUBLIC_FIREBASE_AUTH_DOMAIN || '',
  projectId: process.env.EXPO_PUBLIC_FIREBASE_PROJECT_ID || '',
  storageBucket: process.env.EXPO_PUBLIC_FIREBASE_STORAGE_BUCKET || '',
  messagingSenderId: process.env.EXPO_PUBLIC_FIREBASE_SENDER_ID || '',
  appId: process.env.EXPO_PUBLIC_FIREBASE_APP_ID || '',
};

if (!config.apiKey || !config.projectId) {
  console.warn('⚠️  EXPO_PUBLIC_FIREBASE_* env vars not set — generating SW with empty config');
}

const swContent = `/**
 * Firebase Messaging Service Worker
 * AUTO-GENERATED by scripts/generate-firebase-sw.js — do NOT edit manually.
 * Handles push notifications when the PWA is in the background or closed.
 * This file MUST be served at the root of the web app.
 */

/* eslint-disable no-undef */
importScripts('https://www.gstatic.com/firebasejs/10.12.0/firebase-app-compat.js');
importScripts('https://www.gstatic.com/firebasejs/10.12.0/firebase-messaging-compat.js');

firebase.initializeApp({
  apiKey: '${config.apiKey}',
  authDomain: '${config.authDomain}',
  projectId: '${config.projectId}',
  storageBucket: '${config.storageBucket}',
  messagingSenderId: '${config.messagingSenderId}',
  appId: '${config.appId}',
});

const messaging = firebase.messaging();

// Handle background push notifications
messaging.onBackgroundMessage((payload) => {
  const { title, body } = payload.notification || payload.data || {};

  const notificationTitle = title || 'Nova notificação';
  const notificationOptions = {
    body: body || '',
    icon: '/app/assets/images/icon.png',
    badge: '/app/assets/images/icon.png',
    data: payload.data,
  };

  self.registration.showNotification(notificationTitle, notificationOptions);
});

// Handle notification click — open the app
self.addEventListener('notificationclick', (event) => {
  event.notification.close();

  event.waitUntil(
    clients.matchAll({ type: 'window', includeUncontrolled: true }).then((clientList) => {
      for (const client of clientList) {
        if (client.url.includes('/app') && 'focus' in client) {
          return client.focus();
        }
      }
      return clients.openWindow('/app');
    })
  );
});
`;

const outPath = path.resolve(__dirname, '..', 'public', 'firebase-messaging-sw.js');
fs.writeFileSync(outPath, swContent, 'utf-8');
console.log(`✅ Generated ${outPath} (project: ${config.projectId || 'NOT SET'})`);
